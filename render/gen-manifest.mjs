// Scan public/<id>/editplan.json and write src/manifest.ts so Root can register one
// browsable composition per video in Remotion Studio. Run before `studio`.
import { readdirSync, existsSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const pub = resolve("public");
const ids = existsSync(pub)
  ? readdirSync(pub).filter((d) => existsSync(resolve(pub, d, "editplan.json")))
  : [];

const body = `// AUTO-GENERATED by gen-manifest.mjs. Do not edit by hand.
export const VIDEO_IDS: string[] = ${JSON.stringify(ids, null, 2)};
`;
writeFileSync(resolve("src", "manifest.ts"), body);
console.log(`wrote src/manifest.ts with ${ids.length} video id(s)`);
